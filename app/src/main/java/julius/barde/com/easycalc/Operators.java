public class Operators {
    public double Add(double fNum,double sNum ){

        return fNum+sNum;


    }

    public double Minus(double fNum,double sNum ){

        return fNum-sNum;


    }
    public double Multiply(double fNum,double sNum ){

        return fNum*sNum;


    }
    public double Divide(double fNum,double sNum ){

        return fNum/sNum;


    }
}
